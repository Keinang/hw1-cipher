package CipertextAttack;

public class StringFreq {
	private String word_;
	private int times_;
	
	public StringFreq(String word, int times) {
		super();
		this.word_ = word;
		this.times_ = times;
	}

	public String getWord_() {
		return word_;
	}

	public void setWord_(String word_) {
		this.word_ = word_;
	}

	public int getTimes_() {
		return times_;
	}

	public void setTimes_(int times_) {
		this.times_ = times_;
	}
	
	
}
